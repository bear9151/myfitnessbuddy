    {
        // If Logged in
        if(Auth::check()){
            $user_id = auth()->user()->id;
            $meals = Meal::orderby('created_at','desc')->where('user_id', $user_id)->whereDate('created_at', '=', date('Y-m-d'))->get();
            return view('dashboard', compact ('meals'));
        }

        return view('pages.index');
    }

